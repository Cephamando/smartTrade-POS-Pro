$lines = explode("\n", $csvData);
$insertedCount = 0;

$pdo->beginTransaction();
try {
    $stmt = $pdo->prepare("INSERT INTO products (name, price, category_id, type) VALUES (?, ?, ?, 'item')");
    $checkStmt = $pdo->prepare("SELECT id FROM products WHERE name = ?");

    foreach ($lines as $line) {
        $line = trim($line);
        if (empty($line)) continue;
        
        $parts = str_getcsv($line);
        if (count($parts) < 2) continue;
        
        $name = trim($parts[0]);
        $price = floatval(preg_replace('/[^0-9.]/', '', $parts[1]));
        
        // Smart Categorization
        $catId = 1; // Default to Beverages
        $nameLower = strtolower($name);
        if (strpos($nameLower, 'nshima') !== false || strpos($nameLower, 'platter') !== false) { 
            $catId = 2; // Meals
        } elseif (strpos($nameLower, 'cabanosis') !== false || strpos($nameLower, 'crisps') !== false || strpos($nameLower, 'crumbe') !== false) { 
            $catId = 4; // Snacks
        } elseif (strpos($nameLower, 'paper') !== false || strpos($nameLower, 'bleach') !== false || strpos($nameLower, 'spray') !== false || strpos($nameLower, 'calcium') !== false || strpos($nameLower, 'chloride') !== false || strpos($nameLower, 'tee-pol') !== false || strpos($nameLower, 'salt') !== false) { 
            $catId = 5; // Cleaning/Other
        }

        // Avoid duplicates
        $checkStmt->execute([$name]);
        if ($checkStmt->rowCount() == 0) {
            $stmt->execute([$name, $price, $catId]);
            $insertedCount++;
        }
    }

    echo "[SUCCESS] Inserted $insertedCount new products.\n";

    // Initialize inventory for all products across all locations
    echo "[INFO] Initializing stock tracking (setting to 0) for all locations...\n";
    $pdo->exec("
        INSERT IGNORE INTO inventory (product_id, location_id, quantity)
        SELECT p.id, l.id, 0
        FROM products p CROSS JOIN locations l
    ");

    $pdo->commit();
    echo "\n🎉 IMPORT COMPLETED SUCCESSFULLY!\n";
    echo "You can now safely delete the src/import_data.php file.\n";

} catch (Exception $e) {
    $pdo->rollBack();
    echo "\n❌ Error: " . $e->getMessage() . "\n";
}
